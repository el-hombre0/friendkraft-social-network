    подвергнуты целой серии независимых исследований.</p>

<p>Противоположная точка зрения подразумевает, что непосредственные участники технического прогресса призывают нас к
    новым свершениям, которые, в свою очередь, должны быть объективно рассмотрены соответствующими инстанциями. Учитывая
